export type UserLevel = 'beginner' | 'intermediate' | 'advanced' | 'warrior';

export interface WodFormat {
  id: string;
  name: string;
  name_es: string;
  type: 'rounds_for_time' | 'amrap' | 'emom' | 'chipper' | 'ladder'
      | 'tabata' | 'interval' | 'couplet' | 'triplet' | 'buy_in_out' | 'death_by' | 'double_wod'
      | 'fgb' | 'e2mom' | 'e3mom' | 'pyramid' | 'single_movement';
  minLevel: 1 | 2 | 3;
  movements: number;
  timecap: string;
  scaling: {
    beginner?: { reps?: string; rounds?: number; rest?: string };
    intermediate?: { reps?: string; rounds?: number; rest?: string };
    advanced?: { reps?: string; rounds?: number; rest?: string };
    warrior?: { reps?: string; rounds?: number; rest?: string };
  };
  note: string;
  note_es: string;
  scalingNote: string;
  scalingNote_es: string;
  description: string;
  description_es: string;
}

export interface WodMovement {
  name: string;
  reps: string;
  load: 'heavy' | 'light' | 'BW';
  minLevel: 1 | 2 | 3;
  demoSearch: string;
  demoChannel: string;
  contraindications?: string[];
  equipment?: string;
}

