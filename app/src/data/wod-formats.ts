export function getWodMovements(level: UserLevel): WodMovement[] {
  switch (level) {
    case 'beginner': return beginnerMovements;
    case 'intermediate': return [...beginnerMovements, ...intermediateMovements];
    case 'advanced': return [...beginnerMovements, ...intermediateMovements, ...advancedMovements];
    case 'warrior': return [...beginnerMovements, ...intermediateMovements, ...advancedMovements];
  }
}

export function getWodFormats(level: UserLevel): WodFormat[] {
  const levelNum = level === 'beginner' ? 1 : level === 'intermediate' ? 2 : 3;
  return wodFormats.filter(f => f.minLevel <= levelNum);
}

export function filterWodMovementsByRestrictions(
  movements: WodMovement[],
  restrictions: string[],
): WodMovement[] {
  if (restrictions.length === 0) return movements;
  return movements.filter(m => {
    if (!m.contraindications) return true;
    return !m.contraindications.some(c => restrictions.includes(c));
  });
}

