// Helper functions
export function getExercisesByPattern(pattern: Pattern): Exercise[] {
  return exercises.filter(e => e.pattern === pattern);
}

export function filterByLevel(exercises: Exercise[], maxLevel: Level): Exercise[] {
  return exercises.filter(e => e.level <= maxLevel);
}

export function filterByRestrictions(exercises: Exercise[], restrictions: string[]): Exercise[] {
  if (restrictions.length === 0) return exercises;
  return exercises.filter(e => {
    if (!e.contraindications) return true;
    return !e.contraindications.some(c => restrictions.includes(c));
  });
}
